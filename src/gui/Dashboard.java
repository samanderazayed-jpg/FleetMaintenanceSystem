     private void refreshTable() {
    DefaultTableModel model = (DefaultTableModel) fleetTable.getModel();
    model.setRowCount(0);
    model.setColumnIdentifiers(new String[]{"Type", "VIN", "Model", "Year", "Odometer (KM)"});

    for (Vehicle v : manager.getFleetList()) {
        // فحص نوع الكائن برمجياً وتحديد النص المناسب للجدول
        String type = "Unknown";
        if (v instanceof model.LuxuryCar) {
            type = "Luxury Car";
        } else if (v instanceof model.Truck) {
            type = "Truck";
        }
        
        model.addRow(new Object[]{type, v.getVin(), v.getModel(), v.getYear(), v.getCurrentOdometer()});
    }
}

